package Project1;

/**
 * 
 * 
 *
 * This class defines some (public) fixed objects that represent the different results
 * possible for a student taking a module.
 * 
 * DO NOT ALTER THIS CLASS
 * 
 * You can write expressions like:  
 *   if (record.result() == ModuleResult.FAIL) ...
 *   
 *   The above expression is comparing the object returned by a result() method call
 *   with the fixed object that represents a module fail, and if they are the same object ...
 *   
 */
public enum ModuleResult
{
    PASS,  FAIL
}
